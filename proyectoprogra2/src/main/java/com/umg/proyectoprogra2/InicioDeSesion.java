    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        String usuario = txtUsuario.getText();
        String password = new String(txtPassword.getPassword());
        String sentencia = "SELECT 1 FROM TBLUSUARIOS WHERE USUARIO = '" + usuario + "' AND PASSWORD = '" + password + "'";

        String urlBd = "jdbc:oracle:thin:@localhost:1521/xepdb1";
        String usuarioBd = "PROYECTO";
        String passwordBd = "proyecto";

        try {
            // Registra/carga el driver
            Class.forName("oracle.jdbc.driver.OracleDriver");

            // Crea una nueva conexi칩n con la configuraci칩n de las variables de la clase (url, usuario, passowrd)
            Connection con = DriverManager.getConnection(urlBd, usuarioBd, passwordBd);

            // Ejecuta la sentencia
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sentencia);

            // Imprime los resultados
            if (!rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(this, "Ingreso Incorrecto", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                rs.next();
                if (rs.getString(1).equals("1")) {
                    JOptionPane.showMessageDialog(this, "Ingreso Correcto", "OK", JOptionPane.INFORMATION_MESSAGE);
                }
            }

            // Cierra la conexi칩n
            con.close();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex, "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

